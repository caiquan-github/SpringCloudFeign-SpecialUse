/**
 * @author Kwon
 * @Title:
 * @Description:
 * @date 2020/10/2816:08
 */
@SpringBootApplication
@EnableEurekaServer
public class EurekaApplication {
    public static void main(String[] args) {
        SpringApplication.run(EurekaApplication.class,args);
    }
}

